/*
* O comando "TRUNCATE".
*/

/*
* O comando "TRUNCATE" faz a deleção de TODOS os registros de uma tabela de forma irreversível, ou seja,
* limpa totalmente a tabela, inclusive disponibilizando novamente os índices das chaves primárias.
* O "TRUNCATE" não faz uso de qualquer cláusula (restrição) para sua aplicação. Deve-se
* ter bastante atenção e certeza na sua utilização. Exemplo abaixo:
*/
TRUNCATE TABLE pessoa;



